import { queryCustomer, removeCustomer, addCustomer, updateCustomer } from '@/services/user';

export default {
  namespace: 'customer',

  state: {
    // data: {
    //   list: [],
    //   pagination: {},
    // },
  },

  effects: {
    
    *fetch({ payload }, { call, put }) {
      const response = yield call(queryCustomer, payload);
      yield put({
        type: 'save',
        payload: response,
      });
    },
    *add({ payload, callback }, { call, put }) {
      const response = yield call(addCustomer, payload);
      yield put({
        type: 'save',
        payload: response,
      });
      if (callback) callback();
    },
    *remove({ payload, callback }, { call, put }) {
      const response = yield call(removeCustomer, payload);
      yield put({
        type: 'save',
        payload: response,
      });
      if (callback) callback();
    },
    *update({ payload, callback }, { call, put }) {
      const response = yield call(updateCustomer, payload);
      yield put({
        type: 'save',
        payload: response,
      });
      if (callback) callback();
    },
  },

  reducers: {
    save(state, action) {
      return {
        ...state,
        data: action.payload,
      };
    },
  },
};
